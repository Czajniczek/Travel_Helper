package com.example.travelhelper;

public class NeedRide {

    private String id, sFromCity, sFromStreet, sToCity, sToStreet, sDay, sMonth, sYear, sHour, sMinute, userId;

    public NeedRide() {
    }

    public String getId() {
        return id;
    }

    public void setId(String userId) {
        this.id = id;
    }

    public String getsFromCity() {
        return sFromCity;
    }

    public void setsFromCity(String sFromCity) {
        this.sFromCity = sFromCity;
    }

    public String getsFromStreet() {
        return sFromStreet;
    }

    public void setsFromStreet(String sFromStreet) {
        this.sFromStreet = sFromStreet;
    }

    public String getsToCity() {
        return sToCity;
    }

    public void setsToCity(String sToCity) {
        this.sToCity = sToCity;
    }

    public String getsToStreet() {
        return sToStreet;
    }

    public void setsToStreet(String sToStreet) {
        this.sToStreet = sToStreet;
    }

    public String getsDay() {
        return sDay;
    }

    public void setsDay(String sDay) {
        this.sDay = sDay;
    }

    public String getsMonth() {
        return sMonth;
    }

    public void setsMonth(String sMonth) {
        this.sMonth = sMonth;
    }

    public String getsYear() {
        return sYear;
    }

    public void setsYear(String sYear) {
        this.sYear = sYear;
    }

    public String getsHour() {
        return sHour;
    }

    public void setsHour(String sHour) {
        this.sHour = sHour;
    }

    public String getsMinute() {
        return sMinute;
    }

    public void setsMinute(String sMinute) {
        this.sMinute = sMinute;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }
}
